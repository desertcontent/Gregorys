		<h3>History of Gregory's Insurance</h3>
<p>Gregory’s Insurance was founded by T. Clifford Gregory during the Great Depression in l932. The first policy was a fire only policy with an annual premium of $3.18.  Mr. Gregory operated by himself through the 30’s sharing office space with attorney, William H. Hein, above the old Alliance National Bank, now First National Bank.</p>
<p><img width="250" height="151" src="/images/Alliance_Office_416-West-3rd.png" alt="Alliance_Office_416-West-3rd" style="float: right;">After the war in 1949, Cliff hired Loren Solberg. They increased business enough to warrant building a small office at 416 West 3rd Street in 1954.   The corporation known as Gregory’s Inc. was founded that same year.</p>
<p>In 1966 Dan Contonis started working for Gregory’s and Duane Beal followed in 1971.  Mr. Gregory retired in 1972 and Mr. Solberg became the sole owner.  In 1974, two small offices were added to the North side of the building at 416 West 3rd.</p>
<p><img width="250" height="191" src="/images/Hemmingford_Office.png" alt="Hemmingford_Office" style="margin-right: 10px; float: left;">In 1975 Gregory’s Insurance purchased the Reinders Agency in Hemingford and in 1977 Steven    Engelhaupt was hired.&nbsp;    In 1978 a branch office was built and opened in Hemingford.&nbsp; The Hemingford branch office was closed in December 2011.&nbsp; Marty Petersen joined the agency in 1980 and is now the current manager of the agency.</p>
<p>In August 1984 Dan and Duane bought Loren Solberg’s stock in Gregory’s and in January 1985, Gary Tomlin purchased the Brittan Agency and merged it with Gregory’s Insurance.  The year 1985 brought a computerized Agency Management System to Gregory’s.  In 1986  three more offices and a reception area were added to the east side of the building at 416 West 3rd. In January 1995, Duane Beal retired</p>
<p>Over the years, Gregory’s added staff and purchased several small agencies in Alliance.  In 1993, Gregory’s Insurance purchased the Babcock-Usher agency in Chadron.  In 1997 the agency was   sold to the Larson Insurance Agency.</p>
<p><img width="250" height="177" src="/images/don_gary.png" alt="don_gary" style="margin: 10px; border: 1px solid #000000; float: left;">In January 2001, Dan and Gary sold Gregory’s to First National of Nebraska, Inc., the parent  company of First National Bank of North Platte. With new ownership, a new corporation, Gregory’s  Insurance Inc. was formed.   In April 2001, Gregory’s Insurance sold the building at  416 West 3rd Street and moved to the second floor of the First National Bank North Platte building at  124 West 3rd Street.</p>
<p>In December 2003, Jon Daniels was hired in Chadron, Nebraska where he had an office in the First National Bank North Platte building in Chadron.   In January 2007 Gregory’s leased office space at 234 East 3rd Street and later that year moved next door at 240 East 3rd Street in Chadron where we are currently located.</p>
<p><img src="/images/2012_management_team.png" alt="management_team" style="border: 1px solid #000000; margin: 10px; float: right;">Dan Contonis retired in 2007 and Gary Tomlin retired in 2008.  In January 2008 a new  management team was formed -   Marty Petersen, general manager as well as the   health and life department manager; Steve Engelhaupt, farm, ranch and crop insurance department manager; Jennifer Wood, personal lines manager; MalaDonn Schuman, commercial lines manager; and Marlene Grier, operations manager.   In September 2010, MalaDonn  Schuman resigned and Jon Daniels became the commercial lines manager</p>
<p>In 2012 Marlene Grier retired and Debra Kumpf was hired as an administrative assistant.</p>
<p>Currently Gregory’s staff consists of thirteen employees in Alliance and three employees in Chadron, Nebraska to serve your needs.</p>
<p>Gregory’s Insurance has been a member of the Independent Insurance Agents of Nebraska for over 50 years.  As an  independent agency, we can offer you a wide range of insurance products to cover your business, home, auto, life and health.</p>

		
		
